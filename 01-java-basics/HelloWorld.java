/**
 * This is a classic "Hello, World!" program in Java.
 * It's the traditional first program for learning a new language.
 * The goal is to print the text "Hello, World!" to the console.
 */
public class HelloWorld {

    /**
     * The main method is the entry point of any Java application.
     * The code inside this method is executed when the program is run.
     *
     * @param args Command line arguments (not used in this program).
     */
    public static void main(String[] args) {
        // System.out is an object that represents the standard output stream (the console).
        // println() is a method that prints a line of text to the console,
        // followed by a new line character.
        System.out.println("Hello, World!");
    }
}
